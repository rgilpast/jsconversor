
export function fromKGToLB(kg) {
  return kg * 2.20462;
}

export function fromLBToKG(lb) {
  return lb * 0.453592;
}

export function fromKmToMi(km) {
  return km * 0.621371;
}

export function fromMiToKm(mi) {
  return mi * 1.60934;
}

export function fromLToGal(l) {
  return l * 0.264172;
}

export function fromGalToL(gal) {
  return gal * 3.78541;
}
